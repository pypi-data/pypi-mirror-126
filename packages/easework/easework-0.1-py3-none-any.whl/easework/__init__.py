def add(num1 , num2):
	return num1 + num2

def substract(num1 , num2):
	return num1 - num2

def multiply(num1 , num2):
	return num1 * num2

def divide(num1, num2):
	return num1 / num2

def sq(num):
	return num * num 

def cb(num):
	return num * num * num 

def sqrt(num):
	return num ** 0.5 

def cbrt(num):
	return num ** 1/3